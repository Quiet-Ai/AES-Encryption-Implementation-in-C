// Key schedule
void key_schedule(const uint8_t key[16], uint8_t round_keys[176]) {
    uint8_t temp[4];
    uint8_t i = 0;
    
    for (i = 0; i < 16; i++) {
        round_keys[i] = key[i];
    }

    for (i = 16; i < 176; i += 4) {

        temp[0] = round_keys[i - 4];
        temp[1] = round_keys[i - 3];
        temp[2] = round_keys[i - 2];
        temp[3] = round_keys[i - 1];

        // Key schedule core
        if (i % 16 == 0) {
            uint8_t k;
            uint8_t t = temp[0];
            temp[0] = sbox[temp[1]] ^ rcon[i / 16];
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[t];
        }

        // XOR with previous round key
        round_keys[i] = round_keys[i - 16] ^ temp[0];
        round_keys[i + 1] = round_keys[i - 15] ^ temp[1];
        round_keys[i + 2] = round_keys[i - 14] ^ temp[2];
        round_keys[i + 3] = round_keys[i - 13] ^ temp[3];
    }
}

// SubBytes
void sub_bytes(uint8_t state[16]) {
    for (int i = 0; i < 16; i++) {
        state[i] = sbox[state[i]];
    }
}

// ShiftRows
void shift_rows(uint8_t state[16]) {
    uint8_t temp;

    // Rotate row 1
    temp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = temp;

    // Rotate row 2
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    // Rotate row 3
    temp = state[3];
    state[3] = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = temp;
}

// MixColumns
void mix_columns(uint8_t state[16]) {
    uint8_t temp[16];
    for (int i = 0; i < 4; i++) {
        temp[4 * i] = mul2[state[4 * i]] ^ mul3[state[4 * i + 1]] ^ state[4 * i + 2] ^ state[4 * i + 3];
        temp[4 * i + 1] = state[4 * i] ^ mul2[state[4 * i + 1]] ^ mul3[state[4 * i + 2]] ^ state[4 * i + 3];
        temp[4 * i + 2] = state[4 * i] ^ state[4 * i + 1] ^ mul2[state[4 * i + 2]] ^ mul3[state[4 * i + 3]];
        temp[4 * i + 3] = mul3[state[4 * i]] ^ state[4 * i + 1] ^ state[4 * i + 2] ^ mul2[state[4 * i + 3]];
    }
    memcpy(state, temp, 16);
}

// AddRoundKey
void add_round_key(uint8_t state[16], const uint8_t round_key[16]) {
    for (int i = 0; i < 16; i++) {
        state[i] ^= round_key[i];
    }
}

// AES encryption
void aes_encrypt(uint8_t state[16], const uint8_t round_keys[176]) {
    add_round_key(state, round_keys); // Initial round key addition

    for (int round = 1; round < NUM_ROUNDS; round++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, &round_keys[round * 16]);
    }

    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, &round_keys[NUM_ROUNDS * 16]);
}

void pad_plaintext(char *input, uint8_t *output, size_t *len) {
    size_t input_len = strlen(input);
    *len = (input_len + AES_BLOCK_SIZE - 1) / AES_BLOCK_SIZE * AES_BLOCK_SIZE;
    memset(output, 0, *len);
    memcpy(output, input, input_len);
}

// Main function
int main() {
    uint8_t key[16] = {0x00};
    uint8_t round_keys[176];
    uint8_t state[16];
    uint8_t padded_plaintext[32];
    size_t padded_len;

    // Generate round keys
    key_schedule(key, round_keys);

    // Read plaintext input
    char input[32];
    printf("Enter to encrypt :3 (up to 16 characters): ");
    fgets(input, 32, stdin);
    input[strcspn(input, "\n")] = '\0'; 

    // Pad and prepare plaintext
    pad_plaintext(input, padded_plaintext, &padded_len);

    // Encrypt each block
    for (size_t i = 0; i < padded_len; i += AES_BLOCK_SIZE) {
        memcpy(state, &padded_plaintext[i], AES_BLOCK_SIZE);
        aes_encrypt(state, round_keys);

        // Print encrypted block
        printf("Encrypted block: ");
        for (int j = 0; j < AES_BLOCK_SIZE; j++) {
            printf("%02X ", state[j]);
        }
        printf("\n");
    }

    return 0;
}
